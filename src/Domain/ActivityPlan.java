package Domain;

public class ActivityPlan {
    int id_activityPlan;
    int id_practicing;
    int id_professor;
    
    public ActivityPlan() {}

    public int getId_activityPlan() {
        return id_activityPlan;
    }

    public void setId_activityPlan(int id_activityPlan) {
        this.id_activityPlan = id_activityPlan;
    }

    public int getId_practicing() {
        return id_practicing;
    }

    public void setId_practicing(int id_practicing) {
        this.id_practicing = id_practicing;
    }

    public int getId_professor() {
        return id_professor;
    }

    public void setId_professor(int id_professor) {
        this.id_professor = id_professor;
    }
    
    
}
